package it.unibo.ristoDB.view;

public class BestProducts {

    private String name;
    private int total;

    public BestProducts(String name, int total) {
        this.name = name;
        this.total = total;
    }

    public String getName() {
        return name;
    }

    public int getTotal() {
        return total;
    }
        
    
}
